adicao = a + b
print('adição:',adicao)
sub  = a - b
#realizando da operaçao subtraçao
print('subtração:',sub)
mult = a*b
#realizando da operaçao multiplicão
print('Multiplicação:',mult)
div = a/b
#realizando da operaçao divisão
print('divisão:',div)
modulo = a % b
#realizando da operaçao modelo
print('Modelo:',modulo)
#realizando da operaçao pontecial
pontecial = a**b
print('Pontecial',pontecial)